// Returns { id, uri } of the best hero image for a destination (for CachedImage)
// CachedImage will resolve local bundled images from PLACE_IMAGES using the id.
export function getDestinationImage(destKey) {
  const places = PLACES_BY_DESTINATION[destKey] || [];
  const best = places.find(p => p.mustVisit) || places[0];
  if (!best) return null;
  return { id: best.id, uri: best.image };
}

// Fuzzy match destination string to a key in PLACES_BY_DESTINATION
export function matchDestination(destination) {
  if (!destination) return null;
  const d = destination.toLowerCase();
  if (d.includes('baguio'))                                                                                    return 'Baguio';
  if (d.includes('vigan') || (d.includes('ilocos') && d.includes('sur')))                                    return 'Vigan';
  if (d.includes('pangasinan') || d.includes('dagupan') || d.includes('alaminos') || d.includes('bolinao')) return 'Pangasinan';
  if (d.includes('dingalan') || d.includes('aurora'))                                                         return 'Dingalan';
  if (d.includes('gabaldon') || d.includes('nueva ecija'))                                                    return 'Gabaldon';
  if (d.includes('atok') || (d.includes('benguet') && !d.includes('ilocos')))                                return 'Atok';
  if (d.includes('tagaytay') || d.includes('taal'))                                                           return 'Tagaytay';
  if (d.includes('ilocos norte') || d.includes('laoag') || d.includes('paoay') || d.includes('pagudpud') || d.includes('bangui')) return 'Ilocos Norte';
  if (d.includes('batanes') || d.includes('batan') || d.includes('sabtang') || d.includes('basco'))          return 'Batanes';
  if (d.includes('bohol') || d.includes('panglao') || d.includes('tagbilaran'))                              return 'Bohol';
  if (d.includes('cebu') || d.includes('moalboal') || d.includes('oslob') || d.includes('badian'))           return 'Cebu';
  if (d.includes('siargao') || d.includes('general luna'))                                                    return 'Siargao';
  if (d.includes('boracay') || d.includes('aklan') || d.includes('malay'))                                   return 'Boracay';
  if (d.includes('el nido') || d.includes('elnido'))                                                          return 'El Nido';
  if (d.includes('coron') || d.includes('busuanga'))                                                          return 'Coron';
  if (d.includes('puerto princesa') || d.includes('puerto') || d.includes('palawan'))                        return 'Puerto Princesa';
  if (d.includes('davao') || d.includes('samal'))                                                             return 'Davao';
  if (d.includes('camiguin') || d.includes('mambajao'))                                                       return 'Camiguin';
  if (d.includes('bukidnon') || d.includes('malaybalay') || d.includes('dahilayan') || d.includes('manolo fortich')) return 'Bukidnon';
  return null;
}
