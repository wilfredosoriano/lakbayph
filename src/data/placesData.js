// Returns { id, uri } of the best hero image for a destination (for CachedImage)
export function getDestinationImage(destKey) {
  const places = PLACES_BY_DESTINATION[destKey] || [];
  const best = places.find(p => p.mustVisit && p.image) || places.find(p => p.image);
  if (!best) return null;
  return { id: best.id, uri: best.image };
}

// Fuzzy match destination string to a key in PLACES_BY_DESTINATION
export function matchDestination(destination) {
  if (!destination) return null;
  const d = destination.toLowerCase();
  if (d.includes('coron'))                            return 'Coron';
  if (d.includes('el nido') || d.includes('elnido')) return 'El Nido';
  if (d.includes('siargao'))                          return 'Siargao';
  if (d.includes('bohol') || d.includes('panglao'))  return 'Bohol';
  if (d.includes('baguio'))                           return 'Baguio';
  if (d.includes('vigan'))                            return 'Vigan';
  if (d.includes('sagada'))                           return 'Sagada';
  if (d.includes('banaue'))                           return 'Banaue';
  if (d.includes('pangasinan') || d.includes('dagupan') || d.includes('alaminos') || d.includes('bolinao')) return 'Pangasinan';
  if (d.includes('palawan'))                          return 'El Nido';
  return null;
}
